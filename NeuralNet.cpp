#include "NeuralNet.h"

#include <iostream>


/********************************************************************************************************************/
/*																													*/
/********************************************************************************************************************/

NeuralNet::NeuralNet()
: m_nInputs( 0 )
{
}


/********************************************************************************************************************/
/*																													*/
/********************************************************************************************************************/

//! @param	nInputs		Number of inputs.
//! @param	nOutputs	Number of outputs

NeuralNet::NeuralNet( int nInputs, int nOutputs )
	: m_nInputs( nInputs ),
	m_aOutputs( nOutputs, 0.f )
{
}


/********************************************************************************************************************/
/*																													*/
/********************************************************************************************************************/

NeuralNet::~NeuralNet()
{
	// Nothing to do
}


/********************************************************************************************************************/
/*																													*/
/********************************************************************************************************************/

//! @param	out		The output stream.
//! @param	nn		The NeuralNet to output.

std::ostream & operator<<( std::ostream & out, NeuralNet const & nn )
{
	out << nn.m_nInputs << ' ' << (int)nn.m_aOutputs.size();

	return out;
}


/********************************************************************************************************************/
/*																													*/
/********************************************************************************************************************/

//! @param	in		The input stream.
//! @param	nn		The NeuralNet to input.

std::istream & operator>>( std::istream & in, NeuralNet & nn )
{
	int	size;
	in >> nn.m_nInputs >> size;

	nn.m_aOutputs.resize( size, 0.f );

	return in;
}
